package com.ternsip.placemod;

/* BlocPos class is classic in 1.8+ version */
public class BlockPos {

    private int x, y, z;

    public BlockPos(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    int getX() {
        return x;
    }

    int getY() {
        return y;
    }

    int getZ() {
        return z;
    }

}
